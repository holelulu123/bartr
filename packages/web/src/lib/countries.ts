/** Map of country code → Country for quick lookup. */
export const COUNTRY_MAP = new Map(COUNTRIES.map((c) => [c.code, c]));

/** Get flag emoji for a country code, or empty string if unknown. */
export function getCountryFlag(code: string | null | undefined): string {
  if (!code) return '';
  return COUNTRY_MAP.get(code.toUpperCase())?.flag ?? '';
}

/** Get country name for a country code, or empty string if unknown. */
export function getCountryName(code: string | null | undefined): string {
  if (!code) return '';
  return COUNTRY_MAP.get(code.toUpperCase())?.name ?? '';
}
