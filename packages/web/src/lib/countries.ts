/** ISO 3166-1 alpha-2 country list with flag emojis. */

export interface Country {
  code: string;
  name: string;
  flag: string;
}

/** Derive flag emoji from a 2-letter ISO country code (regional indicator symbols). */
function flag(code: string): string {
  return String.fromCodePoint(
    ...code.toUpperCase().split('').map((c) => 0x1f1e6 + c.charCodeAt(0) - 65),
  );
}

function c(code: string, name: string): Country {
  return { code, name, flag: flag(code) };
}

